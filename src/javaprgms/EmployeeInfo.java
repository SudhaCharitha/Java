package javaprgms;

public class EmployeeInfo {

		private int empID;
		private String empName;
		
	//setter and getter
		public int getEmpID() {
			return empID;
		}
		public void setEmpID(int empID) {
			this.empID = empID;
		}
		public String getEmpName() {
			return empName;
		}
		public void setEmpName(String empName) {
			this.empName = empName;
		}	
		
	}
